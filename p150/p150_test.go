import (
	"testing"
)

func TestEvalRPN(t *testing.T) {
	testcases := []struct {
		tokens []string
		want   int
	}{
		//{[]string{"2", "1", "+", "3", "*"}, 9},
		//{[]string{"4", "13", "5", "/", "+"}, 6},
		//{[]string{"10", "6", "9", "3", "+", "-11", "*", "/", "*", "17", "+",
		//	"5", "+"}, 22},
		{[]string{"-78", "-33", "196", "+", "-19", "-", "115", "+", "-", "-99",
			"/", "-18", "8", "*", "-86", "-", "-", "16", "/", "26", "-14", "-",
			"-", "47", "-", "101", "-", "163", "*", "143", "-", "0", "-", "171",
			"+", "120", "*", "-60", "+", "156", "/", "173", "/", "-24", "11",
			"+", "21", "/", "*", "44", "*", "180", "70", "-40", "-", "*", "86",
			"132", "-84", "+", "*", "-", "38", "/", "/", "21", "28", "/", "+",
			"83", "/", "-31", "156", "-", "+", "28", "/", "95", "-", "120", "+",
			"8", "*", "90", "-", "-94", "*", "-73", "/", "-62", "/", "93", "*",
			"196", "-", "-59", "+", "187", "-", "143", "/", "-79", "-89", "+",
			"-"}, 165},
	}

	for _, tc := range testcases {
		got := evalRPN(tc.tokens)
		if got != tc.want {
			t.Errorf("evalRPN(%v) = %v, want %v", tc.tokens, got, tc.want)
		}
	}
}
